class AnsiStyle:
    RESET = "\033[0m"
    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    REVERSED = "\033[7m"

    # Foreground colors
    FG_BLACK = "\033[30m"
    FG_RED = "\033[31m"
    FG_GREEN = "\033[32m"
    FG_YELLOW = "\033[33m"
    FG_BLUE = "\033[34m"
    FG_MAGENTA = "\033[35m"
    FG_CYAN = "\033[36m"
    FG_WHITE = "\033[37m"

    # Bright foreground colors
    FG_BRIGHT_RED = "\033[1;31m"
    FG_BRIGHT_GREEN = "\033[1;32m"
    FG_BRIGHT_YELLOW = "\033[1;33m"
    FG_BRIGHT_BLUE = "\033[1;34m"
    FG_BRIGHT_MAGENTA = "\033[1;35m"
    FG_BRIGHT_CYAN = "\033[1;36m"
    FG_BRIGHT_WHITE = "\033[1;37m"

    # Background colors
    BG_BLACK = "\033[40m"
    BG_RED = "\033[41m"
    BG_GREEN = "\033[42m"
    BG_YELLOW = "\033[43m"
    BG_BLUE = "\033[44m"
    BG_MAGENTA = "\033[45m"
    BG_CYAN = "\033[46m"
    BG_WHITE = "\033[47m"

